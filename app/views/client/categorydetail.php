<script>
    var url_category = window.location.href;
    const getcategory_id = url_category.split('/');
    var category_id = getcategory_id[5];
    var coursesGetPostByID = 'http://localhost:8000/api/post/category' + '/' + category_id;
    var coursesGetAllAPI = 'http://localhost:8000/api/post';

    function start() {
        getCourses(function(courses) {
            renderCourses(courses)
        });
        getAllPost(function(allCourses) {
            renderAllpost(allCourses)
        });
    }
    start();

    function getCourses(callback) {
        fetch(coursesGetPostByID)
            .then(function(response) {
                return response.json();
            })
            .then(callback);
    }

    function getAllPost(callback) {
        fetch(coursesGetAllAPI)
            .then(function(response) {
                return response.json();
            })
            .then(callback);
    }

    function renderAllpost(courses) {
        var source =
            document.querySelector('#all-post');
        var allpost = courses.map(function(course) {
            return `
        <div class="row" style="margin-top: 10px;">
                            <div class="col-md-5">
                                <a href="detail.html">
                                    <img class="img-responsive" src="${course.image}" alt="">
                                </a>
                            </div>
                            <div class="col-md-7">
                                <a href="#"><b>${course.title}</b></a>
                            </div>
                            <p  class="demo-here">${course.body}</p>
                            <div class="break"></div>
                        </div>
        `
        });
        source.innerHTML = allpost.join('');
    }

    function renderCourses(courses) {
        var listCourseBlock =
            document.querySelector('#demo-something');
        var htmls = courses.map(function(course) {
            return `
        <h3>
        <a href="#"> ${course.category_name} </a>
        </h3>
        <div class="col-md-12 border-right">
            <div class="col-md-3">
                <a href="chitiet.html">
                <img class="img-responsive" src="${course.image}" alt="">
                </a>
            </div>
            <div class="col-md-9">
                <h3>${course.title}</h3>
                <p  class="demo-here">${course.body}</p>
                <a class="btn btn-primary" href="detail/${course.id}">Details <span class="glyphicon glyphicon-chevron-right"></span></a>
            </div>
            <div class="break"></div>
        </div>
        `
        });
        listCourseBlock.innerHTML = htmls.join('');
    }
</script>




